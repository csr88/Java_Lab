package com.company;

public class AllJavaOperators {
    public static void main(String[] args) {

        //Arithmetic Operators

        int a = 10;
        int b = 20;
        System.out.println(a+=b);
        System.out.println(a-=b);
        System.out.println(a*=b);
        System.out.println(a/=b);
        System.out.println(a%=b);
    }


}
